# importing math module
import math

# factorial
factorial_5 = math.factorial (5)
factorial_8 = math.factorial (8)

# printing results
print (f'Use of factorial () function:')
print (f'  5! = 1 * 2 * 3 * 4 * 5\n     = {factorial_5}')
print (f'  8! = 1 * 2 * 3 * 4 * 5 * 6 * 7 * 8\n     = {factorial_8}')
print (f'')
