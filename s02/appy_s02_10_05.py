# importing math module
import math

# two floats "a" and "b"
a = 12.34
b = -56.78

# fabs
a_abs = math.fabs (a)
b_abs = math.fabs (b)

# printing results
print (f'Use of fabs () function:')
print (f'  a                   = {a}')
print (f'  absolute value of a = {a_abs}')
print (f'  b                   = {b}')
print (f'  absolute value of b = {b_abs}')
print (f'')
