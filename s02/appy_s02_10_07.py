# importing math module
import math

# two integers "c" and "d"
c = 30
d = 45

# lcm
lcm_c_d = math.lcm (c, d)

# printing results
print (f'Use of lcm () function:')
print (f'  c                                = {c}')
print (f'  d                                = {d}')
print (f'  least common multiple of c and d = {lcm_c_d}')
print (f'')
