# importing math module
import math

# two integers "c" and "d"
c = 30
d = 45

# gcd
gcd_c_d = math.gcd (c, d)

# printing results
print (f'Use of gcd () function:')
print (f'  c                                  = {c}')
print (f'  d                                  = {d}')
print (f'  greatest common divisor of c and d = {gcd_c_d}')
print (f'')
